def get_week_questions(week_number):
    """Get questions for a specific week"""
    week_key = f"Week {week_number}"
    return quiz_data.get(week_key, [])

def get_all_questions():
    """Get all questions from all weeks"""
    all_questions = []
    for week in quiz_data.values():
        all_questions.extend(week)
    return all_questions

def get_total_questions():
    """Get total number of questions"""
    return sum(len(questions) for questions in quiz_data.values())

def generate_mixed_quiz(num_questions=50):
    """Generate a mixed quiz with random questions from all weeks"""
    import random

    # Collect all questions in a list
    all_questions = []
    for week, questions in quiz_data.items():
        for question in questions:
            # Add week information to each question for reference
            question_with_source = question.copy()
            question_with_source['source_week'] = week
            all_questions.append(question_with_source)

    # Randomly select questions
    selected_questions = random.sample(all_questions, min(num_questions, len(all_questions)))

    return selected_questions
